def main():
    parser = argparse.ArgumentParser(
        description="Generate mapper version shim for Vendor15 survival."
    )
    parser.add_argument(
        "--vendor-version", type=int, default=4,
        help="Vendor mapper version (default: 4)",
    )
    parser.add_argument(
        "--target-version", type=int, default=5,
        help="Target mapper version to shim (default: 5)",
    )
    parser.add_argument(
        "--output-dir", type=str, default=".",
        help="Output directory for generated files",
    )

    args = parser.parse_args()
    vendor_ver = args.vendor_version
    target_ver = args.target_version
    output_dir = Path(args.output_dir)

    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"Generating mapper v{target_ver} shim (wrapping vendor v{vendor_ver})...")

    # Generate files
    files = {
        f"mapper_v{target_ver}_shim.h": generate_header(vendor_ver, target_ver),
        f"mapper_v{target_ver}_shim.cpp": generate_source(vendor_ver, target_ver),
        "Android.bp": generate_android_bp(vendor_ver, target_ver),
        "README.md": generate_readme(vendor_ver, target_ver),
    }

    for filename, content in files.items():
        filepath = output_dir / filename
        with open(filepath, "w") as f:
            f.write(content)
        print(f"  Created: {filepath}")

    print(f"\nDone. Files written to: {output_dir}")
    print(f"\nNext steps:")
    print(f"  1. Copy to $AOSP/hardware/interfaces/graphics/mapper/shim/")
    print(f"  2. Add SELinux file_contexts entry")
    print(f"  3. Build: m mapper.v{target_ver}.0-impl-shim")
    print(f"  4. Verify: adb logcat | grep MapperV{target_ver}Shim")


if __name__ == "__main__":
    main()
