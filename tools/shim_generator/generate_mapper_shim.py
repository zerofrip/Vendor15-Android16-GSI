def generate_header(vendor_ver: int, target_ver: int) -> str:
    """Generate the shim header file."""
    return textwrap.dedent(f"""\
        /*
         * AUTO-GENERATED by generate_mapper_shim.py
         * Mapper v{target_ver} shim wrapping vendor v{vendor_ver}
         *
         * DO NOT EDIT MANUALLY
         */
        #pragma once

        #include <android/hardware/graphics/mapper/utils/IMapperProvider.h>
        #include <dlfcn.h>
        #include <log/log.h>

        #define SHIM_LOG_TAG "MapperV{target_ver}Shim"

        namespace android::hardware::graphics::mapper::shim {{

        /**
         * Mapper v{target_ver} shim that delegates to vendor's v{vendor_ver} implementation.
         *
         * This shim is loaded by libui when it looks for mapper v{target_ver}.0.
         * It internally dlopen's the vendor's v{vendor_ver}.0 mapper and translates
         * v{target_ver} calls to v{vendor_ver} calls.
         *
         * For v{target_ver} methods that have no v{vendor_ver} equivalent, the shim
         * returns AIMAPPER_ERROR_UNSUPPORTED, allowing the framework to degrade
         * gracefully.
         */

        class MapperV{target_ver}Shim {{
        public:
            static MapperV{target_ver}Shim& getInstance();

            bool isLoaded() const {{ return mHandle != nullptr; }}

            // v{vendor_ver} functions (delegated)
            AIMapper_Error importBuffer(const native_handle_t* handle,
                                        buffer_handle_t* outBuffer);
            AIMapper_Error freeBuffer(buffer_handle_t buffer);
            AIMapper_Error lock(buffer_handle_t buffer, uint64_t cpuUsage,
                               ARect accessRegion, int acquireFence,
                               void** outData);
            AIMapper_Error unlock(buffer_handle_t buffer, int* outReleaseFence);
            AIMapper_Error getTransportSize(buffer_handle_t buffer,
                                            uint32_t* outNumFds,
                                            uint32_t* outNumInts);

            // v{target_ver}-only functions (return UNSUPPORTED)
            AIMapper_Error getReservedRegion(buffer_handle_t buffer,
                                             void** outReservedRegion,
                                             uint64_t* outReservedSize);

        private:
            MapperV{target_ver}Shim();
            ~MapperV{target_ver}Shim();

            bool loadVendorMapper();

            void* mHandle = nullptr;  // dlopen handle

            // Function pointers to vendor v{vendor_ver} implementation
            using ImportBufferFunc = AIMapper_Error (*)(const native_handle_t*, buffer_handle_t*);
            using FreeBufferFunc = AIMapper_Error (*)(buffer_handle_t);
            using LockFunc = AIMapper_Error (*)(buffer_handle_t, uint64_t, ARect, int, void**);
            using UnlockFunc = AIMapper_Error (*)(buffer_handle_t, int*);
            using GetTransportSizeFunc = AIMapper_Error (*)(buffer_handle_t, uint32_t*, uint32_t*);

            ImportBufferFunc mImportBuffer = nullptr;
            FreeBufferFunc mFreeBuffer = nullptr;
            LockFunc mLock = nullptr;
            UnlockFunc mUnlock = nullptr;
            GetTransportSizeFunc mGetTransportSize = nullptr;
        }};

        }}  // namespace android::hardware::graphics::mapper::shim
    """)


def generate_source(vendor_ver: int, target_ver: int) -> str:
    """Generate the shim implementation."""
    # List of vendor .so names to try
    vendor_so_names = [
        f'"mapper.vendor-v{vendor_ver}.0.so"',
        f'"android.hardware.graphics.mapper@{vendor_ver}.0-impl.so"',
        f'"mapper@{vendor_ver}.0-impl.so"',
        f'"hw/mapper.{vendor_ver}.0.so"',
    ]
    vendor_so_list = ",\n        ".join(vendor_so_names)

    return textwrap.dedent(f"""\
        /*
         * AUTO-GENERATED by generate_mapper_shim.py
         * Mapper v{target_ver} shim wrapping vendor v{vendor_ver}
         *
         * DO NOT EDIT MANUALLY
         */

        #include "mapper_v{target_ver}_shim.h"

        #include <dlfcn.h>
        #include <errno.h>
        #include <log/log.h>
        #include <string.h>

        namespace android::hardware::graphics::mapper::shim {{

        MapperV{target_ver}Shim& MapperV{target_ver}Shim::getInstance() {{
            static MapperV{target_ver}Shim instance;
            return instance;
        }}

        MapperV{target_ver}Shim::MapperV{target_ver}Shim() {{
            if (!loadVendorMapper()) {{
                ALOGE("%s: Failed to load vendor mapper v{vendor_ver}. "
                      "Buffer operations will fail.", SHIM_LOG_TAG);
            }} else {{
                ALOGI("%s: Successfully loaded vendor mapper v{vendor_ver} shim.",
                      SHIM_LOG_TAG);
            }}
        }}

        MapperV{target_ver}Shim::~MapperV{target_ver}Shim() {{
            if (mHandle) {{
                dlclose(mHandle);
                mHandle = nullptr;
            }}
        }}

        bool MapperV{target_ver}Shim::loadVendorMapper() {{
            // Try multiple possible .so names for vendor mapper v{vendor_ver}
            const char* soNames[] = {{
                {vendor_so_list}
            }};

            for (const char* soName : soNames) {{
                ALOGI("%s: Trying to load vendor mapper: %s", SHIM_LOG_TAG, soName);

                // RTLD_NOW: resolve all symbols immediately to fail fast
                // RTLD_LOCAL: don't pollute global symbol namespace
                mHandle = dlopen(soName, RTLD_NOW | RTLD_LOCAL);
                if (mHandle) {{
                    ALOGI("%s: Loaded vendor mapper: %s", SHIM_LOG_TAG, soName);
                    break;
                }}

                ALOGW("%s: dlopen(%s) failed: %s", SHIM_LOG_TAG, soName, dlerror());
            }}

            if (!mHandle) {{
                ALOGE("%s: Could not load any vendor mapper v{vendor_ver} .so", SHIM_LOG_TAG);
                return false;
            }}

            // Resolve function pointers
            // The actual symbol names depend on the vendor's export table.
            // Common patterns: AIMapper_loadIMapper, HIDL IMapper methods, etc.
            //
            // For gralloc/mapper v{vendor_ver}, the entry point is typically:
            //   AIMapper_loadIMapper() which returns an AIMapper struct with function pointers
            //
            // We resolve via the standard AIMapper loading mechanism.

            using LoadIMapperFunc = AIMapper_Error (*)(AIMapper* _Nullable* _Nonnull);
            auto loadIMapper = reinterpret_cast<LoadIMapperFunc>(
                dlsym(mHandle, "AIMapper_loadIMapper"));

            if (!loadIMapper) {{
                ALOGE("%s: AIMapper_loadIMapper not found in vendor .so: %s",
                      SHIM_LOG_TAG, dlerror());
                dlclose(mHandle);
                mHandle = nullptr;
                return false;
            }}

            AIMapper* mapper = nullptr;
            AIMapper_Error err = loadIMapper(&mapper);
            if (err != AIMAPPER_ERROR_NONE || !mapper) {{
                ALOGE("%s: AIMapper_loadIMapper failed: %d", SHIM_LOG_TAG, err);
                dlclose(mHandle);
                mHandle = nullptr;
                return false;
            }}

            // Extract function pointers from the AIMapper struct
            mImportBuffer = mapper->v5.importBuffer ?
                mapper->v5.importBuffer : mapper->importBuffer;
            mFreeBuffer = mapper->v5.freeBuffer ?
                mapper->v5.freeBuffer : mapper->freeBuffer;
            mLock = mapper->v5.lock ? mapper->v5.lock : mapper->lock;
            mUnlock = mapper->v5.unlock ? mapper->v5.unlock : mapper->unlock;
            mGetTransportSize = mapper->v5.getTransportSize ?
                mapper->v5.getTransportSize : mapper->getTransportSize;

            // Validate minimum function set
            if (!mImportBuffer || !mFreeBuffer || !mLock || !mUnlock) {{
                ALOGE("%s: Vendor mapper missing critical functions", SHIM_LOG_TAG);
                dlclose(mHandle);
                mHandle = nullptr;
                return false;
            }}

            return true;
        }}

        // ============================================================
        // Delegated v{vendor_ver} functions
        // ============================================================

        AIMapper_Error MapperV{target_ver}Shim::importBuffer(
                const native_handle_t* handle, buffer_handle_t* outBuffer) {{
            if (!mImportBuffer) return AIMAPPER_ERROR_UNSUPPORTED;
            return mImportBuffer(handle, outBuffer);
        }}

        AIMapper_Error MapperV{target_ver}Shim::freeBuffer(buffer_handle_t buffer) {{
            if (!mFreeBuffer) return AIMAPPER_ERROR_UNSUPPORTED;
            return mFreeBuffer(buffer);
        }}

        AIMapper_Error MapperV{target_ver}Shim::lock(
                buffer_handle_t buffer, uint64_t cpuUsage,
                ARect accessRegion, int acquireFence, void** outData) {{
            if (!mLock) return AIMAPPER_ERROR_UNSUPPORTED;
            return mLock(buffer, cpuUsage, accessRegion, acquireFence, outData);
        }}

        AIMapper_Error MapperV{target_ver}Shim::unlock(
                buffer_handle_t buffer, int* outReleaseFence) {{
            if (!mUnlock) return AIMAPPER_ERROR_UNSUPPORTED;
            return mUnlock(buffer, outReleaseFence);
        }}

        AIMapper_Error MapperV{target_ver}Shim::getTransportSize(
                buffer_handle_t buffer, uint32_t* outNumFds, uint32_t* outNumInts) {{
            if (!mGetTransportSize) return AIMAPPER_ERROR_UNSUPPORTED;
            return mGetTransportSize(buffer, outNumFds, outNumInts);
        }}

        // ============================================================
        // v{target_ver}-only functions — return UNSUPPORTED
        // ============================================================
        // These methods exist in v{target_ver} but have no v{vendor_ver} equivalent.
        // Returning UNSUPPORTED allows the framework to degrade gracefully.

        AIMapper_Error MapperV{target_ver}Shim::getReservedRegion(
                buffer_handle_t /*buffer*/, void** /*outReservedRegion*/,
                uint64_t* /*outReservedSize*/) {{
            ALOGW("%s: getReservedRegion not supported in vendor v{vendor_ver} shim",
                  SHIM_LOG_TAG);
            return AIMAPPER_ERROR_UNSUPPORTED;
        }}

        }}  // namespace android::hardware::graphics::mapper::shim
    """)


